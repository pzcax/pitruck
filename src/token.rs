#[derive(Debug, Clone, PartialEq)]
pub enum TemplatePart {
    Str(String),
    Code(Vec<(Token, usize, usize)>),
}

#[derive(Debug, Clone, PartialEq)]
pub enum Token {
    Number(f64),
    StringLit(String),
    TemplateStr(Vec<TemplatePart>),
    Ident(String),

    Var,
    Bring,
    Func,
    Return,
    If,
    Elif,
    Else,
    While,
    For,
    In,
    Print,
    Class,
    Self_,
    Match,
    Break,
    Continue,
    Try,
    Catch,
    And,
    Or,
    Not,
    True,
    False,
    Null,

    Plus,
    Minus,
    Star,
    Slash,
    Percent,
    EqEq,
    BangEq,
    Lt,
    Gt,
    LtEq,
    GtEq,
    Eq,
    PlusEq,
    MinusEq,
    StarEq,
    SlashEq,

    LBrace,
    RBrace,
    LParen,
    RParen,
    LBracket,
    RBracket,
    Comma,
    Dot,
    FatArrow,
    Colon,
    Question,
    QuestionQuestion,
    OptDot,
    OptLBracket,
    Underscore,

    EOF,
}